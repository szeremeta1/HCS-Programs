public class Circle {

   public static void main(String[] args) {
      double radius = 420.0;
      System.out.println("Radius: " + radius);
      System.out.println("Circumference: " + 2 * Math.PI * radius);
      System.out.println("Area: " + Math.PI * Math.pow(radius, 2));
   }
}